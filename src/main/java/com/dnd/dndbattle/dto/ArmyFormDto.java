package com.dnd.dndbattle.dto;

public class ArmyFormDto {

    public Integer armyId;

    public Integer soldierId;

    public Integer size;

    public Integer playerId;

    public Integer getArmyId() {
        return armyId;
    }

    public void setArmyId(Integer armyId) {
        this.armyId = armyId;
    }

    public Integer getSoldierId() {
        return soldierId;
    }

    public void setSoldierId(Integer soldierId) {
        this.soldierId = soldierId;
    }

    public Integer getSize() {
        return size;
    }

    public void setSize(Integer size) {
        this.size = size;
    }

    public Integer getPlayerId() {
        return playerId;
    }

    public void setPlayerId(Integer playerId) {
        this.playerId = playerId;
    }
}
